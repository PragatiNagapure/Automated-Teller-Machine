    private void WithdrawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WithdrawButtonActionPerformed
        // TODO add your handling code here:
         String amountText = withdrawAmountField.getText();
    
    if (amountText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter an amount.");
        return;
    }

    try {
        int amount = Integer.parseInt(amountText);
        if (amount <= 0) {
            JOptionPane.showMessageDialog(this, "Enter a valid amount.");
            return;
        }

        Connection con = DBConnection.getConnection();

        // For demo, assuming only one user exists
        Statement st = con.createStatement();
       // String username="vaibhavi";
        ResultSet rs = st.executeQuery("SELECT balance FROM users WHERE username='"+Session.username+"'");

        if (rs.next()) {
            double currentBalance = rs.getDouble("balance");
            if (amount > currentBalance) {
                JOptionPane.showMessageDialog(this, "Insufficient balance.");
            } else {
                double newBalance = currentBalance - amount;
                PreparedStatement ps = con.prepareStatement("UPDATE users SET balance=? WHERE username=?");
                ps.setDouble(1, newBalance);
                ps.setString(2,Session.username);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Withdrawal successful!");
  }
        }

        con.close();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter numbers only.");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_WithdrawButtonActionPerformed

